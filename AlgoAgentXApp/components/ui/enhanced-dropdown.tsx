// Enhanced dropdown with auto-close behavior
interface AutoCloseDropdownProps {
  children: React.ReactNode
  open: boolean
  onOpenChange: (open: boolean) => void
  onClose?: () => void
}

export function AutoCloseDropdown({ 
  children, 
  open, 
  onOpenChange,
  onClose 
}: AutoCloseDropdownProps) {
  React.useEffect(() => {
    if (!open) return

    const handleRouteChange = () => {
      onOpenChange(false)
      onClose?.()
    }

    const handleClickOutside = (event: MouseEvent) => {
      const target = event.target as Element
      if (!target.closest('[data-radix-dropdown-menu-content]')) {
        onOpenChange(false)
        onClose?.()
      }
    }

    const handleEscapeKey = (event: KeyboardEvent) => {
      if (event.key === 'Escape') {
        onOpenChange(false)
        onClose?.()
      }
    }

    // Listen for route changes (if using a router)
    // This would need to be adapted based on your routing setup
    const handlePopState = () => {
      onOpenChange(false)
      onClose?.()
    }

    document.addEventListener('mousedown', handleClickOutside)
    document.addEventListener('keydown', handleEscapeKey)
    window.addEventListener('popstate', handlePopState)

    return () => {
      document.removeEventListener('mousedown', handleClickOutside)
      document.removeEventListener('keydown', handleEscapeKey)
      window.removeEventListener('popstate', handlePopState)
    }
  }, [open, onOpenChange, onClose])

  return (
    <DropdownMenu open={open} onOpenChange={onOpenChange}>
      {children}
    </DropdownMenu>
  )
}

